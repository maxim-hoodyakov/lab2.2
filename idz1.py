#!/usr/bin/env python3
# -*- coding: utf-8 -*-

if __name__ == '__main__':
    n = int(input("Введите число экзаменов: "))
    if n == 1:
        print("Мы успешно сдали", n, "экзамен")
    elif 2 <= n <= 4:
        print("Мы успешно сдали", n, "экзамена")
    elif 5 <= n <= 20:
        print("Мы успешно сдали", n, "экзаменов")
    else:
        print("Всего 20 экзаменов, мы не могли сдать 21 экзамен или более.")